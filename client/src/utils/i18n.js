export const translate = (language, key, replacements = {}) => {
  const fallbackLanguage = 'vi';
  const template = translations?.[language]?.[key] ?? translations?.[fallbackLanguage]?.[key] ?? key;

  if (typeof template !== 'string') {
    return key;
  }

  return Object.entries(replacements).reduce((result, [token, value]) => {
    return result.replaceAll(`{${token}}`, String(value ?? ''));
  }, template);
};
