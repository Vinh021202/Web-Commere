export const getLanguageConfig = (language) =>
  LANGUAGE_OPTIONS[language] || LANGUAGE_OPTIONS.VN;

export const translate = (language, key, fallback = key) =>
  translations[key]?.[language] || fallback;

export const formatCurrencyByLanguage = (language, value) => {
  const { locale, currency } = getLanguageConfig(language);
  const numericValue = Number(value || 0);

  return numericValue.toLocaleString(locale, {
    style: "currency",
    currency,
    minimumFractionDigits: 0,
    maximumFractionDigits: 0,
  });
};

export const formatNumberByLanguage = (language, value) => {
  const { locale } = getLanguageConfig(language);
  const numericValue = Number(value || 0);

  return numericValue.toLocaleString(locale);
};
